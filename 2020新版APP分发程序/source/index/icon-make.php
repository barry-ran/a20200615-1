// 获取不同倍率，提高图形清晰度
function _getScaleBySize (size) {
    var scale = window.devicePixelRatio || 1;
    scale = size > 500 ? 4 : scale;
    scale = size < 100 ? 1 : scale;
    scale = size < 50 ? 0.5 : scale;
    return scale;
}


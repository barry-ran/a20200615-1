// cutting -> 图像不保存直接跳转到裁切功能页
function download (e) {
    var cutting = e.cutting;
    var shareContent = document.getElementById("priviewIcon");
    var width = shareContent.offsetWidth;  // dom宽度
    var height = shareContent.offsetHeight;
    // var offsetTop = shareContent.offsetTop + 999;  // 元素距离顶部的偏移量
    var canvas = document.createElement('canvas');
    canvas.style.width = width + "px";
    canvas.style.height = height + "px";
    var context = canvas.getContext('2d');
    var imgsize = cutting ? 1024 : $(this).data('size');
    var scaleBy = _getScaleBySize(imgsize);
    canvas.width = width * scaleBy;
    canvas.height = height * scaleBy;  // 加上顶部距离，解決高度偏移
    context.scale(scaleBy, scaleBy);
    var imgtype = $('.bg-set-8 .bg-set-check').data('type');

    var html2canvasConfig = {
        canvas: canvas,
        useCORS: true,
        tainttest: true, // 检测图片加载完成
        scale: scaleBy,
        width: width,
        height: height,
        logging: false,
    }

    html2canvas(shareContent, html2canvasConfig).then(function(canvas) {
        // 裁切图片
        if (cutting) {
            _toCuttingImg(Canvas2Image.convertToImage(canvas, imgsize, imgsize, imgtype, imgsize + 'x' + imgsize));
            return;
        }

        // 直接保存
        Canvas2Image.saveAsImage(canvas, imgsize, imgsize, imgtype, imgsize + 'x' + imgsize);
    });
}

